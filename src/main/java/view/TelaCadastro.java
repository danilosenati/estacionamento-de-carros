    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableCarros.getModel();
        modelo.setNumRows(0); // Acaba com a duplicação de valores.
        CarroDAO carroDao = new CarroDAO();

        for(Carro car: carroDao.read()) {

            modelo.addRow(new Object[]{
                car.getId(),
                car.getMarca(),
                car.getPlaca(),
                car.getCor(),
                car.getHoraEntrada(),
                car.getHoraSaida()
            });
        }
    }
    
    
