    private void jButton1ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ExcluirActionPerformed
        // Excluindo itens da tabela:
        
        if(jTableCarros.getSelectedRow()!= -1){ // Significa que o usuário clicou em um item da tabela.
            Carro carro = new Carro();
            CarroDAO carroDao = new CarroDAO();

            carro.setId((int)jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 0));
            
            carroDao.delete(carro);

            //LIMPADO OS CAMPOS APÓS CADASTRAR
            txtMarca.setText("");
            txtPlaca.setText("");
            txtCor.setText("");
            txtHoraEntrada.setText("");
            txtHoraSaida.setText("");

            readJTable();
        }
         else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_jButton1ExcluirActionPerformed

    private void jTableCarrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCarrosMouseClicked
        // TODO add your handling code here:
        
        if(jTableCarros.getSelectedRow() != -1){
            txtMarca.setText(jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 0).toString());
            txtPlaca.setText(jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 1).toString());
            txtCor.setText(jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 2).toString());
            txtHoraEntrada.setText(jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 3).toString());
            txtHoraSaida.setText(jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 4).toString());
        }
    }//GEN-LAST:event_jTableCarrosMouseClicked

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // ATUALIZANDO OS DADOS DA TABELA:
        
        if(jTableCarros.getSelectedRow() != -1){
            Carro carro = new Carro();
            CarroDAO carroDao = new CarroDAO();

            carro.setMarca(txtMarca.getText());
            carro.setPlaca(txtPlaca.getText());
            carro.setCor(txtCor.getText());
            carro.setHoraEntrada(Integer.parseInt(txtHoraEntrada.getText()));
            carro.setHoraSaida(Integer.parseInt(txtHoraSaida.getText()));
            carro.setId((int)jTableCarros.getValueAt(jTableCarros.getSelectedRow(), 0));
            
            carroDao.update(carro);

            //LIMPADO OS CAMPOS APÓS CADASTRAR
            txtMarca.setText("");
            txtPlaca.setText("");
            txtCor.setText("");
            txtHoraEntrada.setText("");
            txtHoraSaida.setText("");

            readJTable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione o produto que deseja alterar.");
        }
        
    }//GEN-LAST:event_jButtonAlterarActionPerformed

